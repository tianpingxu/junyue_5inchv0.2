/**
 * @brief DAC 模块 CPU传输测试函数
 * @param buf 待传输数据
 * @param l 待传输数据长度
 */
void dac_cpu_tran_test(uint16_t *buf, uint32_t l)
{
    uint32_t data_s = 0;
    hal_dac_init(LEFTALIGNED_8BITS, TRIGGER_SOFTWARE, DET_ON_RISING, 0);

    while (1)
    {
        hal_dac_senddata(buf[data_s++]);
        if (data_s == l)
        {
            data_s = 0;
        }
    }
}

/**
 * @brief DAC 模块 DMA 中断配置初始化函数
 *
 */
void dac_dma_irqinit(void)
{
    NVIC_Init(3, 1, DMA2_IRQn, 2);
    DMA2_IRQHandler_Callback = hal_dac_irqhandler;
}

/**
 * @brief DAC 模块PIT定时器初始化函数
 * @param rate 采样率
 */
void dac_pit_init(uint32_t rate)
{
    hal_pit32_handler_t hpit32;

    hpit32.instance = PIT2;

    hpit32.init.counter = hal_pit32_calc_counter(g_ips_clk, PIT32_CLK_DIV_1, rate); // 1ms

    hpit32.init.enable_ie = DISABLE;

    hpit32.init.reload = ENABLE;

    hpit32.init.prescaler = PIT32_CLK_DIV_1;

    hpit32.init.run_at_debug = ENABLE;

    hpit32.init.run_at_doze = ENABLE;

    hpit32.init.update_cnt = ENABLE;

    hal_pit32_init(&hpit32);
}

/**
 * @brief DAC 模块 DMA传输测试函数
 * @param buf 待传输数据
 * @param l 待传输数据长度
 * @param freq 采样率
 * @param mul 是否阻塞
 */
void dac_dma_tran_test(uint8_t *buf, uint32_t l, uint32_t freq, uint8_t mul)
{
    hdma.instance = DMAC2;
    hdma.init.channel_num = 0; // HAL_DMACC_CHANNEL_0;

    hal_dmac_deinit(&hdma);

    hdma.init.dst_msize = HAL_DMAC_MSIZE_4;
    hdma.init.src_msize = HAL_DMAC_MSIZE_4;
    hdma.init.src_inc = HAL_DMAC_SINC_INC;
    hdma.init.dst_inc = HAL_DMAC_DINC_NO_CHANGE;
    hdma.init.dst_tr_width = HAL_DMAC_TR_WIDTH_16BITS;
    hdma.init.src_tr_width = HAL_DMAC_TR_WIDTH_16BITS;

    hdma.init.transfer_type = HAL_DMAC_TRANSFERTYPE_M2P_CONTROLLER_DMA;
    hdma.init.peripheral_type = HAL_DMAC2_PERIPHERAL_DAC; // 需要修改
    hdma.init.transfer_lli = HAL_DMAC_LLP_DIS;
    hdma.init.src_addr = (uint32_t)buf;
    hdma.init.dst_addr = (uint32_t)(DAC->DAC_DR);
    hdma.init.transfer_size = l;

    hal_dac_init(LEFTALIGNED_12BITS, TRIGGER_PIT, DET_ON_RISING, 1);
    hal_dmac_init(&hdma);
    dac_dma_irqinit();

    hal_dmac_start_it(&hdma);

    dac_pit_init(freq);

    if (mul)
    {
        // 阻塞式
        while (dma_tran_complete == 0);
    }
}
